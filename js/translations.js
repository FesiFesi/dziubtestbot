// Funkcja do pobierania t≈Çumaczenia z opcjƒÖ podstawiania warto≈õci
function t(path, values = {}) {
    // NAPRAWIONE: Pobierz aktualny jƒôzyk bezpo≈õrednio z localStorage
    const actualLanguage = localStorage.getItem('gameLanguage') || 
                           (window.gameState?.settings?.language) || 'en';
    
    // Aktualizuj currentLanguage je≈õli siƒô r√≥≈ºni
    if (currentLanguage !== actualLanguage) {
        currentLanguage = actualLanguage;
        console.log("Zaktualizowano jƒôzyk w funkcji t():", currentLanguage);
    }
    
    // Sprawd≈∫ czy t≈Çumaczenia dla jƒôzyka istniejƒÖ
    if (!translations[currentLanguage]) {
        console.error("B≈ÇƒÖd: Brak t≈Çumacze≈Ñ dla jƒôzyka", currentLanguage);
        // NAPRAWIONE: Lepszy fallback
        if (currentLanguage === 'pl' && translations.en) {
            return getTranslation('en', path, values);
        } else if (currentLanguage === 'en' && translations.pl) {
            return getTranslation('pl', path, values);
        }
        return path; // Zwr√≥ƒá ≈õcie≈ºkƒô, je≈õli nie ma t≈Çumacze≈Ñ
    }
    
    return getTranslation(currentLanguage, path, values);
}









// Funkcja pomocnicza do pobierania t≈Çumaczenia
function getTranslation(lang, path, values) {
    const keys = path.split('.');
    let value = translations[lang];
    
    for (const key of keys) {
        if (!value || typeof value !== 'object' || value[key] === undefined) {
            console.warn("Ostrze≈ºenie: Nie znaleziono t≈Çumaczenia dla klucza", path, "w jƒôzyku", lang);
            return path; // Zwr√≥ƒá ≈õcie≈ºkƒô, je≈õli nie znajdzie t≈Çumaczenia
        }
        value = value[key];
    }
    
    // Sprawd≈∫, czy ko≈Ñcowa warto≈õƒá jest obiektem
    if (typeof value === 'object' && value !== null) {
        console.warn("Ostrze≈ºenie: T≈Çumaczenie dla klucza", path, "w jƒôzyku", lang, "jest obiektem, a nie tekstem");
        return path; // Zwr√≥ƒá ≈õcie≈ºkƒô, zamiast obiektu
    }
    
    // Podstaw warto≈õci je≈õli sƒÖ
    if (typeof value === 'string' && values) {
        Object.keys(values).forEach(key => {
            value = value.replace(`{{${key}}}`, values[key]);
        });
    }
    
    return value;
}


















